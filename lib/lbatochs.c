/************************************************************************
 * 提供LBA转换CHS的函数
 ***********************************************************************/
#include "../inc/type.h"
#define cl 20
#define he 16
#define se 63
void ltoc(b32 lba,b32 *C,b32 *H,b32 *S)
{
	*C=lba/(he*se);
	*H=lba%(he*se)/se;
	*S=lba%(he*se)%se+1;
}
